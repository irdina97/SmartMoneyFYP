package com.example.smartmoney;

public class Calculation {

    private String Result;
    private String Date;

    public Calculation() {
    }

    public String getResult() {
        return Result;
    }

    public void setResult(String result) {
        Result = result;
    }

    public String getDate() {
        return Date;
    }

    public void setDate(String date) {
        Date = date;
    }
}
